"""

operadores relacionales nos permiten obtener un valor booleano
verdadero o falso se utiliza para condicionar el codigo

"""

num_uno = 10
num_dos = 20

"""
tipos:
    >  mayor que
    <  menor que
    >= mayor igual que
    <= menor igual que
    == igual igual
    != desigual

"""

resultado = num_uno > num_dos
print(resultado)