

valor_uno = 10
valor_dos = 4

#suma

sum = valor_uno + valor_dos
print(f'el resultado de la suma es:{sum} ')

#resta

resta = valor_uno - valor_dos
print(f'el resultado de la resta es:{resta} ')

#multiplicacion

multi = valor_uno * valor_dos
print(f'el resultado de la multiplicacion es:{multi} ')

#division con numeros decimales

div_uno = valor_uno / valor_dos
print(f'el resultado de la divicion es:{div_uno} ')

#divicion con numero entero

div_dos = valor_uno // valor_dos
print(f'el resultado de la divicion es:{div_dos} ')

